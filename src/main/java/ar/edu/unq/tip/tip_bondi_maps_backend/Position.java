package ar.edu.unq.tip.tip_bondi_maps_backend;

public class Position {

	private float lat;
	private float lng;

	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLng() {
		return lng;
	}
	public void setLng(float lng) {
		this.lng = lng;
	}	
	
}
